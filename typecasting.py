# Typecasting = the process of converting a variable from one data type to another
#               str(), int(), float(), bool()

name = "Vincent Anjiri"
age = 24
gpa = 3.2
is_student = True

print(type(name))
print(type(age))
print(type(gpa))
print(type(is_student))

gpa = int(gpa)
age = float(age)
age = str(age)
name = bool(name)
print(gpa)
print(age)
print(type(age))
print(name)